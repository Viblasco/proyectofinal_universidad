    private void JBuscarIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBuscarIdActionPerformed
        // TODO add your handling code here:
        try {
        int id = Integer.parseInt(jIdAlumno.getText());
        alumno = alumnoD.buscarAlumno(id);
        jNombre.setText(alumno.getNombre());
        jApellido.setText(alumno.getApellido());
        jDNI.setText(Integer.toString(alumno.getDni()));
        jDCFecha.setDate(Date.valueOf(alumno.getFecha_nacimiento()));
        jEstado.setSelected(alumno.isEstado());
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, "Error: Ingrese un id existente");
        }
        
    }//GEN-LAST:event_JBuscarIdActionPerformed

    private void jBuscarDniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBuscarDniActionPerformed
        // TODO add your handling code here:
        try {
        int dni = Integer.parseInt(jDNI.getText());
        alumno = alumnoD.buscarAlumnoporDni(dni);
        jIdAlumno.setText(Integer.toString(alumno.getId()));
        jNombre.setText(alumno.getNombre());
        jApellido.setText(alumno.getApellido());
        jDCFecha.setDate(Date.valueOf(alumno.getFecha_nacimiento()));
        jEstado.setSelected(alumno.isEstado());
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, "Error: Ingrese un DNI existente");
        }
    }//GEN-LAST:event_jBuscarDniActionPerformed

